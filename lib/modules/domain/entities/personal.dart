class Personal {
  final String id;
  final String name;
  final List<String> specialties;
  final double rating;
  final int price;
  final String bio;
  final String photoUrl;
  final String city;
  final String state;
  final String whatsapp;

  const Personal({
    required this.id,
    required this.name,
    required this.specialties,
    required this.rating,
    required this.price,
    required this.bio,
    required this.photoUrl,
    required this.city,
    required this.state,
    required this.whatsapp,
  });
}
